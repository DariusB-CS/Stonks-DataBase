CREATE DATABASE STONKS;
USE STONKS;



CREATE TABLE USERS
(
	Email VARCHAR(15) NOT NULL,
    PassW VARCHAR(15) NOT NULL,
    id CHAR(9) NOT NULL


);

CREATE TABLE STOCKS
(
	Date DATE NOT NULL,
	Token CHAR(4) NOT NULL,
    AdjClose INT NOT NULL,
    CloseP INT NOT NULL,
    HighP INT NOT NULL,
    LowP INT NOT NULL,
    OpenP INT NOT NULL,
    Volume INT NOT NULL
);




